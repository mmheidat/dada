#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*memory;
	unsigned char	value;
	size_t			i;

	i = 0;
	memory = (unsigned char *)b;
	value = (unsigned char)c;
	while (len > i)
	{
		memory[i] = value;
		i++;
	}
	return (b);
}
