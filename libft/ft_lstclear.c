#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*current;
	t_list	*nextone;

	if (!lst || !*lst || !del)
		return ;
	current = *lst;
	while (current)
	{
		nextone = current->next;
		ft_lstdelone(current, del);
		current = nextone;
	}
	*lst = NULL;
}
